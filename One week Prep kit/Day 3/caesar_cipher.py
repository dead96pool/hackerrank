def caesarCipher(s, k):
    # Write your code here
    cipher = ""
    for char in s:
        if char.islower():
            cipher += chr((ord(char)+k-97)%26+97)
        elif char.isupper():
            cipher += chr((ord(char)+k-65)%26+65)
        else:
            cipher += char
    return cipher

